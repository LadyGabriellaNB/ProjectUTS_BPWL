        <h4>Hak Anggota :</h4>
        1. Sarana penelusuran (Kartu katalog maupun OPAC).<br><br>
        2. Sarana ruang baca (buku, majalah, surat kabar, AV dan koleksi langka).<br><br>
        3. Pemesanan koleksi sebanyak 2 (dua) judul khusus untuk buka setiap kali permintaan.<br><br>
        4. Pembuatan reproduksi koleksi baik dalam bentuk foto kopi, rekaman, bentuk mikro maupun digital (untuk jasa
