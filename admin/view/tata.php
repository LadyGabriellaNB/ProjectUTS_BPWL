    <h1>Tata Tertib Perpustakaan</h1><span>Oleh: Pengurus Perpustakaan</span><br>
    <h1>Peraturan Umum</h1>
    1. Berpakaian sopan dan tidak diperkenankan memakai kaos oblong, jaket dan sandal.<br>
    2. Mengisi daftar pengunjung yang sudah disediakan.<br>
    3. Tidak diperkenankan membawa buku, tas, map dan sejenisnya, serta membawa jaket ke ruang perpustakaan.<br>
    4. Tidak diperkenankan menyimpan uang, perhiasan, dan barang-barang berharga lainnya dalam perlengkapan barang yang
    di titipkan.<br>
    5. Menjaga kerapihan bahan pustaka, kebersihan, keamanan dan ketenangan belajar.<br>
    6. Tidak diperkenankan membawa makanan dan minuman atau pun makan-makan dan merokok di ruang perpustakaan.<br>
    7. Memperlihatkan kepada petugas barang/buku yang dibawa pada saat masuk dan keluar perpustakaan.<br>

    <h1>Peraturan Peminjaman Bahan Pustaka</h1>
    <h4>1. Prosedur Peminjaman</h4>
    a. Setiap Peminjam harus memperlihatkan kartu anggota perpustakaan yang masih berlaku.<br>
    b. Setiap Peminjam tidak diperkenankan menggunakan kartu anggota perpustakaan milik orang lain<br>
    c. Jenis koleksi buku yang dipinjam adalah semua buku yang ada di ruang sirkulasi.<br>
    d. Pinjaman untuk di photo copy adalah semua koleksi yang berada di ruang sirkulasi dan referensi<br>
    e. Setiap peminjaman diharuskan memeriksakan keutuhan buku yang akan dipinjamnya kepada petugas peminjaman<br>
    f. Buku-buku yang dipinjam oleh mahasiswa, dosen, karyawan dan mahasiswa Pascasarjana <br>paling lambat dikembalikan
    7 (tujuh) hari terhitung mulai tanggal peminjaman.<br>
    g. Buku yang telah habis masa pinjamannya harus dikembalikan tepat waktunya dan dapat diperpanjang waktu
    pinjamnya.<br>
    h. Perpanjangan waktu peminjaman hanya dapat dilakukan sebanyak 1(satu) kali.<br>
    <h4>2. Kewajiban dan Tanggung Jawab Peminjam</h4>
    a. Peminjam diwajibkan memelihara buku yang dipinjamnya dengan baik dan dilarang membuat tulisan, coretan atau
    merusak/merobek halaman buku.<br>
    b. Kerusakan buku yang dipinjam yang disebabkan oleh peminjam, sepenuhnya menjadi tanggung jawab <br>peminjam dan
    diharuskan mengganti dengan buku yang sama dalam keadaan utuh dan ditambah dengan denda keterlambatan.<br>
    c. Kehilangan buku perpustakaan yang sedang dipinjam sepenuhnya menjadi tanggung jawab peminjam. Penggantian dapat
    berupa:<br>
    Buku yang sama judulnya;<br>
    Uang yang besarnya:<br>
    1 X harga buku, untuk buku-buku terbitan dalam negeri, ditambah biaya administrasi, atau 2 X lipat harga buku, untuk
    buku-buku terbitan dalam negeri yang termasuk kategori buku langka, atau 3 X harga buku, untuk buku-buku terbitan
    luar negeri.<br>
    <h4>3. Sanksi</h4>
    a. Setiap peminjam yang mempunyai buku pinjaman dan telah melewati batas waktu peminjamannya tidak diperkenankan
    meminjam buku lain sebelum buku tersebut dikembalikan.<br>
    b. Setiap peminjaman yang terlambat mengembalikan buku dikenakan denda sesuai dengan ketentuan yang berlaku<br>
    c. Setiap peminjam yang terlambat mengembalikan buku pinjamannya sampai 2(dua) bulan berturut-turut terhitung sejak
    jatuh tempo tanggal pengembaliannya, dinyatakan menghilangkan buku tersebut.<br>
    d. Tidak diperkenankan menggunakan haknya sebagai anggota perpustakaan untuk sementara waktu<br>
    e. Dicabut tanda keanggotaanya dari perpustakaan Politeknik Caltex Riau<br>
    <h4>Ketentuan Khusus</h4>
    a. Setiap Anggota yang keluar dari keanggotaan/mahasiswa yang akan mengambil ijazah, diharuskan mendapat surat
    keterangan bebas pinjaman dari<br> perpustakaan dengan menyerahkan Skripsi berikut Soft Copy nya dalam bentuk CD dan
    diwajibkan menyumbang buku.<br>
    b. Bagi mahasiswa yang telah lulus ujian diharuskan menyerahkan buku ilmiah bermutu atau uang sesuai ketentuan yang
    belaku <br>
